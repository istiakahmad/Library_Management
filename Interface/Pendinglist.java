            String sql = "SELECT `issue_no`, `mid`, `bid`, `i_date`, `r_date`, `mark` FROM `bookissue`"
                        + " WHERE mark='0' and mid LIKE '%"+pbookfind.getText()+"%'";
            pat = (PreparedStatement) con.prepareStatement(sql);
